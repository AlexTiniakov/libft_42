#include "libft.h"

char	*ft_strcat(char *restrict s1, const char *restrict s2)
{
	int i;
	int j;

	i = 0;
	j = -1;
	while (s1[i++] != '\0')
		;
	while (s2[++j] != '\0')
		s1[i++ - 1] = s2[j];
	s1[--i] = s2[j];
	return (s1);
}
