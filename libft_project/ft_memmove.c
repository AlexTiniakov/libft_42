#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*a;
	unsigned char	*b;

	a = (unsigned char *)dst;
	b = (unsigned char *)src;
	if (b < a)
		while (len--)
			a[len] = b[len];
	else
		ft_memcpy(a, b, len);
	return (dst);
}
